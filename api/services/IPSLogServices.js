const IPSLogModel = require('../models/IPSLogSchema');

async function getAllRecordsFromDB() {
    var records = await IPSLogModel.find(function (err, docs) {
        if (err) {
            throw err;
        } else {
            if (docs) {
                console.log('Found all records.');
            } else {
                console.log('No records found.')
            }
        }
    }).clone();

    return records;
}

async function getRecordFromDB(id) {
    var record = await IPSLogModel.findOne({ uid: id }, function (err, doc) {
        if (err) {
            throw err;
        } else {
            if (doc) {
                console.log('Found ' + doc);
            } else {
                console.log('Could not find record with uid: ' + id);
            }
        }
    }).clone();

    return record;
}

async function addRecordToDB(body) {
    var record = new IPSLogModel(body);
    var status = await IPSLogModel.findOne(body, function (err, doc) {
        if (err) {
            throw err;
        } else {
            if (doc) {
                console.log('Record already exists.');
            } else {
                record.save(function (err, doc) {
                    if (err) {
                        throw err;
                    } else {
                        console.log('Added ' + doc);
                    }
                });
            }
        }
    }).clone();

    return status;
}

async function updateRecordInDB(id, body) {
    var status = await IPSLogModel.findOneAndUpdate({ uid: id }, body, function (err, doc) {
        if (err) {
            throw err;
        } else {
            if (doc) {
                console.log('Sucessfully updated the record to: ' + doc);
            } else {
                console.log('No record found to update.');
            }
        }
    }).clone();

    return status;
}

async function deleteRecordFromDB(id) {
    var status = await IPSLogModel.findOne({ uid: id });

    await IPSLogModel.findOneAndDelete({ uid: id }, function (err, doc) {
        if (err) {
            throw err;
        } else {
            if (doc) {
                console.log('Sucessfully deleted record :' + doc);
            } else {
                console.log('No record found to delete.');
            }
        }
    }).clone();

    return status;
}

module.exports = {
    getAllRecordsFromDB,
    getRecordFromDB,
    addRecordToDB,
    updateRecordInDB,
    deleteRecordFromDB
};